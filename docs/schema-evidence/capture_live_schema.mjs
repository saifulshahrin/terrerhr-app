function quoteIdentifier(value) {
  return `"${String(value).replaceAll('"', '""')}"`;
}

function quoteLiteral(value) {
  if (value === null || value === undefined) return 'NULL';
  return `'${String(value).replaceAll("'", "''")}'`;
}

async function query(text, params = []) {
  const result = await client.query(text, params);
  return result.rows;
}

async function writeJson(fileName, value) {
  await fs.writeFile(
    path.join(outputDirectory, fileName),
    `${JSON.stringify(value, null, 2)}\n`,
    'utf8',
  );
}

function renderColumn(column) {
  let definition = `${quoteIdentifier(column.column_name)} ${column.data_type}`;
  if (column.collation_name) {
    definition += ` COLLATE ${quoteIdentifier(column.collation_schema)}.${quoteIdentifier(column.collation_name)}`;
  }
  if (column.generated_kind === 's') {
    definition += ` GENERATED ALWAYS AS (${column.default_expression}) STORED`;
  } else if (column.identity_kind === 'a') {
    definition += ' GENERATED ALWAYS AS IDENTITY';
  } else if (column.identity_kind === 'd') {
    definition += ' GENERATED BY DEFAULT AS IDENTITY';
  } else if (column.default_expression) {
    definition += ` DEFAULT ${column.default_expression}`;
  }
  if (column.not_null) definition += ' NOT NULL';
  return definition;
}

function renderPolicy(policy) {
  const policyRoles = Array.isArray(policy.roles)
    ? policy.roles
    : String(policy.roles || '')
        .replace(/^\{|\}$/g, '')
        .split(',')
        .filter(Boolean);
  const roles = policyRoles
    .map(role => role === 'PUBLIC' ? 'PUBLIC' : quoteIdentifier(role))
    .join(', ');
  let statement = `CREATE POLICY ${quoteIdentifier(policy.policy_name)} ON ${quoteIdentifier(policy.schema_name)}.${quoteIdentifier(policy.table_name)}`;
  statement += ` AS ${policy.permissive ? 'PERMISSIVE' : 'RESTRICTIVE'}`;
  statement += ` FOR ${policy.command}`;
  statement += ` TO ${roles}`;
  if (policy.using_expression) statement += ` USING (${policy.using_expression})`;
  if (policy.check_expression) statement += ` WITH CHECK (${policy.check_expression})`;
  return `${statement};`;
}

function renderGrant(grant, objectType) {
  const grantOption = grant.is_grantable === 'YES' ? ' WITH GRANT OPTION' : '';
  return `GRANT ${grant.privilege_type} ON ${objectType} ${quoteIdentifier(grant.object_schema)}.${quoteIdentifier(grant.object_name)} TO ${quoteIdentifier(grant.grantee)}${grantOption};`;
}

function renderGrantee(grantee) {
  return grantee === 'PUBLIC' ? 'PUBLIC' : quoteIdentifier(grantee);
}

